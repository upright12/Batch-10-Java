package myJavaPackage.loops;

public class LoopPractice2 {
    /*
    There are few types of loops
    1. While loop
    2. For loop
    3. Do-while loop
     */

    public static void whileLoop(){
        // 1-100 print
        int a = 1;
        while (a<=100){
            System.out.println(a);
            a++;
        }

    }


    public static void forLoop(){
       for(int a = 1; a<=100; a++){
           System.out.println(a);
       }
    }





}
